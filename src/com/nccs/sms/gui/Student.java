    private void editBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBttnActionPerformed
        // TODO add your handling code here:
        if(editBttn.getText().equalsIgnoreCase("Edit")) {
            editBttn.setText("Update");
            StudentDTO studentEdit = new StudentDAO().editStudent(table);
            idTxt.setText(String.valueOf(studentEdit.getId()));
            idTxt.setEditable(false);
            fullnameTxt.setText(studentEdit.getFullname());
            dobTxt.setText(studentEdit.getDob());
            genderTxt.setText(studentEdit.getGender());
            addressTxt.setText(studentEdit.getAddress());
            contactTxt.setText(studentEdit.getContact());
            
        } else {
            editBttn.setText("Edit");
            StudentDTO studentUpdate = new StudentDTO();
            studentUpdate.setId(Integer.parseInt(idTxt.getText()));
            studentUpdate.setFullname(fullnameTxt.getText());
            studentUpdate.setDob(dobTxt.getText());
            studentUpdate.setAddress(addressTxt.getText());
            studentUpdate.setContact(contactTxt.getText());
            new StudentDAO().updateStudent(studentUpdate);
            loadData();
            idTxt.setEditable(true);
            clearFields();
        } 
    }//GEN-LAST:event_editBttnActionPerformed

    

