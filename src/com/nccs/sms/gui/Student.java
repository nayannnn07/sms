    private void addBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBttnActionPerformed
        // TODO add your handling code here:
        StudentDTO student = new StudentDTO();
        student.setId(Integer.parseInt(idTxt.getText()));
        student.setFullname(fullnameTxt.getText());
        student.setDob(dobTxt.getText());
        student.setGender(buttonGroup1.getText());
        student.setAddress(addressTxt.getText());
        student.setContact(contactTxt.getText());
        new StudentDAO().addstudent(student);
        loadData();
    }//GEN-LAST:event_addBttnActionPerformed

    private void loadData(){
        try {
            StudentDAO studentDAO = new StudentDAO();
            table.setModel(studentDAO.buildTableModel(studentDAO.getQueryResult()));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
